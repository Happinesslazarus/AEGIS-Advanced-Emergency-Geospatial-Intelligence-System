"""
Extracts a 28-feature daily time-series for every UK sample point (generated by
generate_uk_grid.py) from Google Earth Engine (GEE) -- Google's free cloud
platform for planetary-scale geospatial analysis.  For each point × date
combination it pulls ~24 of the 28 AEGIS schema features from ERA5-Land,
CHIRPS, MODIS, SMAP, SRTM, ESA WorldCover, SoilGrids, HydroSHEDS, and ENSO.

Glossary:
  GEE               = Google Earth Engine -- free cloud geospatial computation
                       platform; requires a free research account
  ee.ImageCollection= GEE object: a time-ordered stack of satellite images
  getRegion()       = GEE batch export method: extracts pixel values at many
                       points in a single API call, far more efficient than
                       individual point queries
  ERA5-Land         = ECMWF climate reanalysis: hourly global estimates of
                       surface meteorological variables at 9 km resolution,
                       back to 1950
  CHIRPS            = Climate Hazards Group InfraRed Precipitation with Station
                       data -- 40-year global rainfall at 0.05° resolution
  MODIS MOD13Q1     = NASA's Moderate Resolution Imaging Spectroradiometer
                       NDVI product -- 16-day composites at 250 m
  SMAP L4           = NASA Soil Moisture Active Passive Level-4: global daily
                       soil moisture at 9 km from 2015
  SRTM              = Shuttle Radar Topography Mission -- near-global 30 m
                       elevation model
  NDVI              = Normalised Difference Vegetation Index -- measure of
                       green vegetation density, range -1 to +1
  NDWI              = Normalised Difference Water Index -- used for flood mapping
  HydroSHEDS        = Hydrological data derived from SRTM; provides catchment
                       boundaries and river network attributes
  chunk             = a batch of ≤100 points; GEE silently fails on large
                       batches so we chunk to stay inside memory limits
  resume checkpoint = JSON file tracking which chunks are already saved so
                      we can restart after network failures without re-fetching
  service account   = a non-human Google Cloud account used for server-side
                       GEE auth without interactive login

  Input  <- data/labels/uk_sample_points.geojson  (from generate_uk_grid.py)
  Output -> data/raw/gee/chunk_NNNN.parquet         (one file per chunk)
  Next   -> scripts/features/build_master_dataset.py  (merges all chunks)

Requirements:
  pip install earthengine-api geemap tqdm pandas pyarrow

Setup:
  Option A -- Interactive (local dev):
    earthengine authenticate
  Option B -- Service account (CI / server):
    export GEE_SERVICE_ACCOUNT=myproject@project.iam.gserviceaccount.com
    export GEE_PRIVATE_KEY_FILE=/path/to/key.json

Usage:
  python scripts/features/gee_extractor.py
  python scripts/features/gee_extractor.py --start 2015-01-01 --end 2024-12-31
  python scripts/features/gee_extractor.py --chunk-size 50 --workers 2
"""

