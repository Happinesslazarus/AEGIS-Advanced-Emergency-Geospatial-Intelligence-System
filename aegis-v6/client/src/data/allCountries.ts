// Build lookup-ready entries with flag emoji
export const ALL_COUNTRIES: CountryEntry[] = RAW_COUNTRIES.map(c => ({
  ...c,
  flag: codeToFlag(c.code),
}))

const CONTINENT_EMOJIS: Record<string, string> = {
  'Africa': '\uD83C\uDF0D',
  'Asia': '\uD83C\uDF0F',
  'Europe': '\uD83C\uDF0D',
  'North America': '\uD83C\uDF0E',
  'South America': '\uD83C\uDF0E',
  'Oceania': '\uD83C\uDF0F',
}

export const CONTINENT_GROUPS: ContinentGroup[] = (() => {
  const order = ['Africa', 'Asia', 'Europe', 'North America', 'South America', 'Oceania']
  return order.map(name => ({
    name,
    emoji: CONTINENT_EMOJIS[name] || '',
    countries: ALL_COUNTRIES.filter(c => c.continent === name).sort((a, b) => a.name.localeCompare(b.name)),
  }))
})()

// Flat alphabetical list of country names
export const ALL_COUNTRY_NAMES: string[] = ALL_COUNTRIES.map(c => c.name).sort()

// Quick lookup: name -> entry
const byName = new Map(ALL_COUNTRIES.map(c => [c.name, c]))
const byCode = new Map(ALL_COUNTRIES.map(c => [c.code, c]))

export function getCountryEntryByName(name: string): CountryEntry | undefined {
  return byName.get(name)
}

export function getCountryEntryByCode(code: string): CountryEntry | undefined {
  return byCode.get(code.toUpperCase())
}

