/* Helper: get flag image URL from ISO code via flagcdn.com */
export function getFlagUrl(code: string, size: 16 | 20 | 24 | 32 | 40 | 48 | 64 = 20): string {
  if (!code) return ''
  //flagcdn uses w20 style for width-based sizing
  return `https://flagcdn.com/w${size}/${code}.png`
}

/* Helper: flat list of all entries with their group */
export function flatRegions(): (RegionEntry & { groupId: string; groupLabel: string })[] {
  return WORLD_REGIONS.flatMap(g =>
    g.entries.map(e => ({ ...e, groupId: g.id, groupLabel: g.label }))
  )
}
